package com.pepcus.apps.exception;

import lombok.Getter;

/**
 * To make an API's error uniquely identifiable by its error code.
 * 
 * @author Sandeep.Vishwakarma
 *
 */
public enum APIErrorCodes {

  // Common Errors
  INTERNAL_SERVER_ERROR(1000),
  INVALID_METHOD_TYPE(1001),
  REQUEST_PARAM_INVALID(1002),
  REQUIRED_PARAMETER(1003),
  UNAUTHORIZED_USER(1004),
  ARGUMENT_TYPE_MISMATCH(1005),
  MEDIA_NOT_SUPPORTED(1006),
  ALREADY_EXISTS(1007),
  ENTITY_NOT_FOUND(1008),
  MALFORMED_JSON_REQUEST(1009),
  ERROR_WRITING_JSON_OUTPUT(1010),
  DATABASE_ERROR(1011),
  NO_RECORDS_FOUND(1012),

  // Validation Error Codes
  VALIDATION_FAILED(1101),
  INVALID_VALUES(1102),
  VALUE_NOT_NUMBER(1103),
  VALUE_NOT_PHONE(1104),
  VALUE_NOT_ALPHABET_ONLY(1105),
  VALUE_LENGTH_TOO_LONG(1106),
  VALUE_BAD_CHARS(1107),
  VALUE_MISSING(1108),
  VALUE_NOT_POS(1109),
  VALUE_NOT_NEG(1110),
  VALUE_NOT_INT(1111),
  VALUE_CANNOT_ZERO(1112),
  VALUE_CANNOT_LESS(1113),
  VALUE_CANNOT_GREATER(1114),
  VALUE_NOT_GREATER(1115),
  VALUE_NOT_LESS(1116),
  VALUE_NOT_EMAIL(1117),
  VALUE_NOT_BETWEEN(1118),
  MISSING_REQUIRED_FIELD(1119),
  INVALID_DATE_FORMAT(1120),
  INVALID_EMAIL(1121),
  INVALID_PHONE(1122),
  DATE_PARSE_ERROR(1123),


  // Search related error code
  INVALID_FIELD_NAME(1201),
  INVALID_OPERATOR(1202),
  INVALID_OPERATOR_ATTRIBUTE(1203),
  INVALID_FIELD_EXPRESSION(1204),



  // Other Error Codes
  ENCRYPTION_ERROR(1301),
  DECRYPTION_ERROR(1302),
  AUTHORIZATION_FAILED(1032),
  ACCESS_DENIED(1051),
  ACCESS_TOKEN_GENERATION_FAILED(1058),
  URL_NOT_FOUND(1092);



  @Getter
  private final Integer code;

  APIErrorCodes(Integer intCode) {
    this.code = intCode;
  }

}
